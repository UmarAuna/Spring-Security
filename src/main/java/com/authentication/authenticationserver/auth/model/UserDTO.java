package com.authentication.authenticationserver.auth.model;

//Define the UserDTO model class as follows. It is responsible
// for getting values from user and passing it to the DAO layer
// for inserting in database.
public class UserDTO {
    private String username;
    private String password;
    private String email;

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
